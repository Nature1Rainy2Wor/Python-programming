a=15
b=4
#Addition+
print("Addition:",a+b)
#Subtraction-
print("Subtraction:",a-b)
#Multiplication*
print("Multiplication:",a*b)
#True Division (float result)/
print("Division:",a/b)
#Floor Division(integer floor)//
print("Floor division:",a//b)
#Modulus(remainder)%
print("Modulo:",a%b)
#Exponentation(power)**
print("Exponentation:",a**b)