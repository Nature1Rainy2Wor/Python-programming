#Implicit Type Casting
x=10
y=2.5


sum=x+y
print(sum)
print("Type of sum is:",type(sum))

#Explicit Type Casting
value="100"
number=int(value)
print("Type of value:",type(number))


f=float(value)
print("Type of f:",type(f))

price=float(value)
price_str=str(price)
print("price is:",price)
print("Type of the price_str is:",type(price_str))
